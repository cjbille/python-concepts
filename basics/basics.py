# convention for variable names to be in snake case
first_name = "chris"

# variable type can be checked
print(type(first_name))

# for loop
for i in range(5):
    print(i)

# while loop
count = 0
while count < 5:
    print(count)
    count += 1

# control flow
isBlue = True
if isBlue:
    print("The color is blue")
else:
    print("The color is not blue")

